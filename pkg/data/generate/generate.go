func schemaGenerating(modulePath string) {
	// Get the root directory of the current module
	moduleDirs, err := utils.GetModuleDirs()
	if err != nil {
		log.Fatalf("failed to get module directory: %v", err)
	}

	// Locate the root directory of the project
	var projectRoot string
	for _, dir := range moduleDirs {
		root, err := utils.GetProjectRoot(dir, "data")
		if err == nil {
			projectRoot = root
			break
		}
	}
	if projectRoot == "" {
		log.Fatal("failed to find valid project root")
	}

	// TODO: remove if no need
	// if err = ensureDependencies(); err != nil {
	// 	log.Fatalf("failed to ensure dependencies: %v", err)
	// }

	schemaDir := filepath.Join(projectRoot, modulePath, entSchemaFolder)

	// fmt.Println("---------------------------------------")
	// fmt.Println("full path:", schemaDir)
	// Load graph from schema
	g, err := entc.LoadGraph(schemaDir, &gen.Config{})
	if err != nil {
		log.Fatalf("failed to load graph from %s: %v", schemaDir, err)
	}
	fmt.Println("ent graph loaded:", g.Schema)
	packageName := filepath.Join(g.Schema, "..", entSchemaGenFolder)
	fmt.Println("ent package name:", packageName)
	fmt.Println("---------------------------------------")

	outputDirs := []string{
		filepath.Join(projectRoot, modulePath, entSchemaGenFolder),
		filepath.Join(projectRoot, modulePath, entHookGenFolder),
		filepath.Join(projectRoot, modulePath, entInterceptorGenFolder),
		filepath.Join(projectRoot, modulePath, entScriptGenFolder),
	}

	for _, outputDir := range outputDirs {
		_ = os.MkdirAll(outputDir, 0755)
	}

	// fmt.Println("\n---------------------------------------")
	generateEntSchema(schemaDir, packageName, outputDirs[0])
	// fmt.Println("\n---------------------------------------")

	tmplGens := map[string]string{
		// ent
		"query_ts_vector": outputDirs[0],
		"query_order_by":  outputDirs[0],
		// "query_filter_by":  outputDirs[0],
		"query_find_first": outputDirs[0],
		"query_tags":       outputDirs[0],
		"runtime_init":     outputDirs[0],

		// ent_hooks
		// "tenant_hook":      outputDirs[1],
		"soft_delete_hook": outputDirs[1],
		"audit_user_hook":  outputDirs[1],
		"register_hooks":   outputDirs[1],

		// ent_interceptors
		"tenant_interceptor":      outputDirs[2],
		"soft_delete_interceptor": outputDirs[2],
		"register_interceptors":   outputDirs[2],

		// sql scripts
		"tsvector_column_generate": outputDirs[3],
	}

	for tmplName, outputDir := range tmplGens {
		// fmt.Println("\n---------------------------------------")
		if tmplName != "tsvector_column_generate" {
			generateTemplate(projectRoot, schemaDir, packageName, outputDir, tmplName, nil)
		} else {
			generateTemplate(projectRoot, schemaDir, packageName, outputDir, tmplName, map[string]interface{}{
				"fileExt": ".sql",
			})
		}
	}

}

