func ensureDependencies() error {
	// TODO: remove kratos log dependency (MUST)
	dependencies := []string{
		"github.com/go-kratos/kratos/v2/log",
		"github.com/jackc/pgx/v5/stdlib",
	}

	for _, dep := range dependencies {
		log.Printf("Ensuring dependency: %s", dep)
		cmd := exec.Command("go", "get", dep)
		cmd.Stdout = os.Stdout
		cmd.Stderr = os.Stderr
		if err := cmd.Run(); err != nil {
			return fmt.Errorf("failed to get dependency %s: %v", dep, err)
		}
	}
	return nil
}

func fetchInfo() (string, string) {
	// Get the root directory of the current module
	moduleDirs, err := utils.GetModuleDirs()
	if err != nil {
		log.Fatalf("failed to get module directory: %v", err)
	}

	// Locate the root directory of the project
	var projectRoot string
	for _, dir := range moduleDirs {
		root, err := utils.GetProjectRoot(dir, "data")
		if err == nil {
			projectRoot = root
			break
		}
	}
	if projectRoot == "" {
		log.Fatal("failed to find valid project root")
	}

	if err = ensureDependencies(); err != nil {
		log.Fatalf("failed to ensure dependencies: %v", err)
	}

	schemaDir := filepath.Join(projectRoot, "data", ignoreDeleteFolder)

	return projectRoot, schemaDir
}

func tmplFuncMap() template.FuncMap {
	return template.FuncMap{
		"sqlKey": func() string { return "entsql" },
		"findRE": func(pattern, s string) [][]string {
			re := regexp.MustCompile(pattern)
			return re.FindAllStringSubmatch(s, -1)
		},
		"dict": func(values ...interface{}) map[string]interface{} {
			dict := make(map[string]interface{})
			if len(values)%2 != 0 {
				panic("invalid dict call: uneven number of args")
			}
			for i := 0; i < len(values); i += 2 {
				key, ok := values[i].(string)
				if !ok {
					panic("dict keys must be strings")
				}
				dict[key] = values[i+1]
			}
			return dict
		},
		"set": func(m map[string]interface{}, key string, value interface{}) string {
			m[key] = value
			return ""
		},
		"split": strings.Split,
		"hasKey": func(m map[string]interface{}, key string) bool {
			_, exists := m[key]
			return exists
		},
		"append": func(slice interface{}, elems ...interface{}) interface{} {
			switch s := slice.(type) {
			case []string:
				for _, e := range elems {
					if str, ok := e.(string); ok {
						s = append(s, str)
					}
				}
				return s
			default:
				return slice
			}
		},
		"join": strings.Join,
		"list": func(args ...interface{}) []string {
			var result []string
			for _, arg := range args {
				if str, ok := arg.(string); ok {
					result = append(result, str)
				}
			}
			return result
		},
		"hasAnnotation": func(ann *gen.Annotations, key string) bool {
			if ann == nil {
				return false
			}
			_, ok := (*ann)[key]
			return ok
		},
		"pascal": func(input string) string {
			words := strings.Split(input, "_")
			for i := range words {
				words[i] = strings.Title(words[i])
				words[i] = format.NormalizeInitialism(words[i])
			}

			return strings.Join(words, "")

		},
		"hasTSVector": func(fields []*gen.Field) bool {
			for _, f := range fields {
				if f.Name == "ts_vector" {
					return true
				}
			}
			return false
		},
		"lower": strings.ToLower,
		"snakeSchema": func(input string) string {
			return strings.ReplaceAll(strings.ToLower(input), "_", "")
		},
		"isIgnoreSQLType": func(fieldType string) bool {
			for _, f := range ignoreSqlTypeFilters {
				if strings.Contains(fieldType, f) {
					return true
				}
			}
			return false
		},
	}
}

func generateEntSchema(schemaDir, packageName, outputDir string) {
	fmt.Println("generate ent schema at: " + outputDir)
	config := &gen.Config{
		Target:  outputDir,
		Package: packageName,
		Header:  "// Code generated by novaerp.com, DO NOT EDIT.",
		Features: []gen.Feature{
			gen.FeaturePrivacy,
			gen.FeatureIntercept,
			gen.FeatureEntQL,
			gen.FeatureNamedEdges,
			gen.FeatureBidiEdgeRefs,
			gen.FeatureSnapshot,
			gen.FeatureSchemaConfig,
			gen.FeatureLock,
			gen.FeatureModifier,
			gen.FeatureExecQuery,
			gen.FeatureUpsert,
			gen.FeatureVersionedMigration,
		},
	}

	err := entc.Generate(schemaDir, config)
	if err != nil {
		log.Fatalf("running ent codegen: %v", err)
	}

	// fmt.Println("ent schema generation successfully")
}

func generateTableMaps(projectRoot, schemaDir, outputDir string) {
	fmt.Println("generateTableMaps: " + outputDir)
	tmplTableMaps := filepath.Join(projectRoot, "data", "generate", "list_module_tables.tmpl")
	tmpl, err := template.New("list_module_tables").Funcs(tmplFuncMap()).ParseFiles(tmplTableMaps)
	if err != nil {
		log.Fatalf("failed to parse template file %s: %v", tmplTableMaps, err)
	}

	graph, err := entc.LoadGraph(schemaDir, &gen.Config{})
	if err != nil {
		log.Fatalf("failed to load graph from schema: %v", err)
	}

	f, err := os.Create(filepath.Join(outputDir, "list_module_tables_gen.go"))
	if err != nil {
		log.Fatalf("failed to create hook output file: %v", err)
	}
	defer f.Close()

	err = tmpl.ExecuteTemplate(f, "list_module_tables", map[string]any{
		"Graph": graph,
	})
	if err != nil {
		log.Fatalf("failed to execute template: %v", err)
	}
	fmt.Println("init table-- generation successfully")
}

func generateTemplate(projectRoot, schemaDir, packageName, outputDir, tmplName string, data map[string]interface{}) {
	// fmt.Println("generate ent template: " + tmplName)
	tmplPath := filepath.Join(projectRoot, "data", "generate", fmt.Sprintf("%s.tmpl", tmplName))
	tmpl, err := template.New(tmplName).Funcs(tmplFuncMap()).ParseFiles(tmplPath)

	if err != nil {
		log.Fatalf("failed to parse template file %s: %v", tmplPath, err)
	}

	graph, err := entc.LoadGraph(schemaDir, &gen.Config{})
	if err != nil {
		log.Fatalf("failed to load graph from schema: %v", err)
	}

	if data == nil {
		data = map[string]interface{}{}
	}

	fileExt, ok := data["fileExt"]
	if !ok || fileExt == "" {
		fileExt = ".go"
	}

	f, err := os.Create(filepath.Join(outputDir, fmt.Sprintf("%s_gen%s", tmplName, fileExt)))
	if err != nil {
		log.Fatalf("failed to create .go output file: %v", err)
	}
	defer f.Close()

	err = tmpl.ExecuteTemplate(f, tmplName, map[string]any{
		"Graph":  graph,
		"Config": &gen.Config{Package: packageName},
	})
	if err != nil {
		log.Fatalf("failed to execute template: %v", err)
	}

	fmt.Println("ent template generation: " + tmplName + " to " + f.Name())
}

